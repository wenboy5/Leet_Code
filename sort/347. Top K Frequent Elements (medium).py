#O(NlogN) O(N)
from collections import defaultdict
class Solution:
    def topKFrequent(self, nums: List[int], k: int) -> List[int]:
        dic = defaultdict(int)
        for i in nums:
            dic[i] += 1
        
        l = []
        for key, val in dic.items():
            l.append((val,key))
        l.sort(key=lambda x:x[0], reverse=True)
        
        res = []
        for i in range(k):
            res.append(l[i][1])
        return res

